enum FireColorPalette currentPalette = FIRE_COLOR_PALETTE_RED;


ColorRGB* FireColor_getPalette() {
    switch (currentPalette) {
        case FIRE_COLOR_PALETTE_RED:
            return FIRE_RED;
        case FIRE_COLOR_PALETTE_YELLOW:
            return FIRE_YELLOW;
        case FIRE_COLOR_PALETTE_BLUE:       
            return FIRE_BLUE;
        case FIRE_COLOR_PALETTE_GREEN:
            return FIRE_GREEN;
        case FIRE_COLOR_PALETTE_PURPLE: 
            return FIRE_PURPLE;
        default:
            return FIRE_RED;
    }
}



void FireColor_nextPalette() {
    currentPalette = (currentPalette + 1) % 5;
}

void FireColor_previousPalette() {
    if (currentPalette == 0) {
        currentPalette = FIRE_COLOR_PALETTE_PURPLE;
    } else {
        currentPalette = (currentPalette - 1) % 5;
    }
}